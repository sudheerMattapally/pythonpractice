name='sudheer kumar'
print(name.upper()) # Converts the string to uppercase
print(name.lower()) # Converts the string to lowercase
print(name.title()) # Converts the first character of each word to uppercase
print(name.capitalize()) # Converts the first character of the string to uppercase
print(name.strip()) # Removes any leading or trailing whitespace
print(name.lstrip()) # Removes leading whitespace
print(name.rstrip()) # Removes trailing whitespace
print(name.replace('sudheer','sudheer kumar')) # Replaces a substring with another substring
print(name.split()) # Splits the string into a list of substrings
print(name.split('s')) # Splits the string into a list of substrings using 's' as the delimiter
print(name.find('s')) # Returns the index of the first occurrence of a substring    
print(name.index('s')) # Returns the index of the first occurrence of a substring   